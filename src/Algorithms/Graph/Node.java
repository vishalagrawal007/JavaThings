package Algorithms.Graph;

public class Node {


    int key;
    int value;

    public Node(int i, int j){
        this.key = i;
        this.value = j;
    }

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
